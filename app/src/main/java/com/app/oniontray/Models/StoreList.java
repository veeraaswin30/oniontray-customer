package com.app.oniontray.Models;


class StoreList {

    private String storeName;
    private int storeImage;
    private int storeItems;
    private String storeRating;
    private String storeDuration;
    private String storeOfferDetail;

    public String getStoreName() {
        return storeName;
    }

    public void setStoreName(String storeName) {
        this.storeName = storeName;
    }

    public int getStoreImage() {
        return storeImage;
    }

    public void setStoreImage(int storeImage) {
        this.storeImage = storeImage;
    }

    public int getStoreItems() {
        return storeItems;
    }

    public void setStoreItems(int storeItems) {
        this.storeItems = storeItems;
    }

    public String getStoreRating() {
        return storeRating;
    }

    public void setStoreRating(String storeRating) {
        this.storeRating = storeRating;
    }

    public String getStoreDuration() {
        return storeDuration;
    }

    public void setStoreDuration(String storeDuration) {
        this.storeDuration = storeDuration;
    }

    public String getStoreOfferDetail() {
        return storeOfferDetail;
    }

    public void setStoreOfferDetail(String storeOfferDetail) {
        this.storeOfferDetail = storeOfferDetail;
    }
}
